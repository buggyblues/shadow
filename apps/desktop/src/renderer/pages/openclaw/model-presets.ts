// ─── Utility Functions ───────────────────────────────────────────────────────

/** Get a provider preset by ID */
export function getProviderPreset(id: string): ProviderPreset | undefined {
  return PROVIDER_PRESETS.find((p) => p.id === id)
}

/** Get recommended models for a provider */
export function getRecommendedModels(providerId: string): ModelPreset[] {
  const preset = getProviderPreset(providerId)
  if (!preset) return []
  return preset.models.filter((m) => m.recommended)
}

/** Get non-deprecated models for a provider */
export function getActiveModels(providerId: string): ModelPreset[] {
  const preset = getProviderPreset(providerId)
  if (!preset) return []
  return preset.models.filter((m) => !m.deprecated)
}

/** Get model preset by id within a provider */
export function getModelPreset(providerId: string, modelId: string): ModelPreset | undefined {
  const preset = getProviderPreset(providerId)
  return preset?.models.find((m) => m.id === modelId)
}

/** Format context window for display (e.g. 128000 → "128K") */
export function formatContextWindow(tokens: number): string {
  if (tokens >= 1000000) {
    const m = tokens / 1000000
    return m === Math.floor(m) ? `${m}M` : `${m.toFixed(1)}M`
  }
  const k = tokens / 1000
  return k === Math.floor(k) ? `${k}K` : `${k.toFixed(1)}K`
}

/** Format price for display (e.g. 2.5 → "$2.50/M") */
export function formatPrice(pricePer1M: number | undefined): string {
  if (pricePer1M === undefined) return '—'
  if (pricePer1M === 0) return '免费'
  if (pricePer1M < 0.01) return `$${pricePer1M.toFixed(4)}`
  if (pricePer1M < 1) return `$${pricePer1M.toFixed(2)}`
  return `$${pricePer1M.toFixed(1)}`
}

/** Format max output tokens */
export function formatMaxTokens(tokens: number): string {
  if (tokens >= 1000) {
    const k = tokens / 1000
    return k === Math.floor(k) ? `${k}K` : `${k.toFixed(1)}K`
  }
  return String(tokens)
}

