/**
 * Comprehensive Model Provider Presets for OpenClaw
 *
 * Contains full metadata for all supported AI model providers including:
 * - Provider configuration (baseUrl, auth type, docs)
 * - Model definitions with context windows, max output tokens, capabilities
 * - Quick setup guides with step-by-step instructions
 * - Provider branding and categorization
 *
 * Data sourced from official provider documentation and OpenClaw integration guides.
 * Last updated: 2026-03-20
 *
 * @see https://docs.openclaw.ai/concepts/model-providers
 */

// ─── Types ───────────────────────────────────────────────────────────────────

export type ModelCapability =
  | 'chat'
  | 'completion'
  | 'vision'
  | 'function-calling'
  | 'json-mode'
  | 'streaming'
  | 'thinking'
  | 'code'
  | 'reasoning'
  | 'web-search'
  | 'file-upload'
  | 'image-generation'
  | 'audio'
  | 'video'
  | 'embedding'

export type ModelTier = 'flagship' | 'balanced' | 'fast' | 'economy' | 'specialized'

export type ProviderCategory = 'global' | 'china' | 'coding-plan' | 'local' | 'custom'

export interface ModelPreset {
  /** Model ID used in API calls */
  id: string
  /** Human-readable display name */
  name: string
  /** Context window size in tokens */
  contextWindow: number
  /** Maximum output tokens */
  maxTokens: number
  /** Supported capabilities */
  capabilities: ModelCapability[]
  /** Model tier / performance class */
  tier: ModelTier
  /** Short description */
  description: string
  /** Whether this model is recommended as default */
  recommended?: boolean
  /** Whether this model is deprecated */
  deprecated?: boolean
  /** Release / knowledge cutoff date */
  releasedAt?: string
  /** Pricing info: input cost per 1M tokens in USD */
  inputPricePer1M?: number
  /** Pricing info: output cost per 1M tokens in USD */
  outputPricePer1M?: number
  /** Pricing info: cached input cost per 1M tokens in USD */
  cachedInputPricePer1M?: number
}

export interface ProviderQuickGuide {
  /** Link to official docs */
  docsUrl: string
  /** Link to get API key */
  apiKeyUrl: string
  /** Step-by-step setup instructions */
  steps: string[]
  /** Additional tips */
  tips?: string[]
  /** Pricing page URL */
  pricingUrl?: string
  /** Coding plan / subscription URL */
  codingPlanUrl?: string
}

/** Valid API format identifiers */
export type ModelApiFormat =
  | 'openai-completions'
  | 'openai-responses'
  | 'openai-codex-responses'
  | 'anthropic-messages'
  | 'google-generative-ai'
  | 'github-copilot'
  | 'bedrock-converse-stream'
  | 'ollama'

/** Coding Plan metadata — fixed-rate subscription plans that aggregate models */
export interface CodingPlanInfo {
  /** Display name */
  name: string
  /** URL to the coding plan page */
  url: string
  /** Description of what the plan includes */
  description: string
  /** Whether this plan includes models from other providers (cross-provider aggregation) */
  crossProvider?: boolean
  /** Model IDs included in this coding plan */
  models: string[]
  /** Pricing description (e.g., "$50/月", "按量计费") */
  pricing?: string
  /** Provider ID alias for the coding plan (e.g., "volcengine-plan") */
  planProviderId?: string
  /** Separate base URL for the coding plan (if different from main provider) */
  baseUrl?: string
  /** API format for the coding plan (if different from main provider) */
  apiFormat?: ModelApiFormat
}

export interface ProviderPreset {
  /** Unique provider ID (used as key in openclaw.json) */
  id: string
  /** Display name */
  name: string
  /** Short description */
  description: string
  /** Provider category */
  category: ProviderCategory
  /** API base URL */
  baseUrl: string
  /** Authentication type */
  auth: 'api-key' | 'oauth' | 'token' | 'none'
  /** API key placeholder hint */
  apiKeyPlaceholder?: string
  /** All available models */
  models: ModelPreset[]
  /** Quick setup guide */
  guide: ProviderQuickGuide
  /** Coding plan info (if available) */
  codingPlan?: CodingPlanInfo
  /** Custom headers if needed */
  defaultHeaders?: Record<string, string>
  /** Whether provider supports OpenAI-compatible API */
  openaiCompatible: boolean
  /** Provider website URL */
  websiteUrl: string
  /** Provider color for branding */
  brandColor: string
  /** API format identifier for OpenClaw (e.g., 'openai-completions', 'anthropic-messages') */
  apiFormat: ModelApiFormat
}

