let _id = 0
const uid = () => `mock-${++_id}`

function makeCard(kind: CardKind, title: string, overrides: Partial<Card> = {}): Card {
  const base: Card = {
    id: uid(),
    kind,
    title,
    summary: `${title} — This is an auto-generated summary for testing card rendering.`,
    content: `Test content: ${title}`,
    sourceId: null,
    linkedCardIds: [],
    meta: {},
    tags: [kind, 'mock'],
    priority: (['high', 'medium', 'low'] as CardPriority[])[Math.floor(Math.random() * 3)],
    autoGenerated: true,
    rating: 3,
    deckIds: [],
    createdAt: new Date().toISOString(),
    updatedAt: new Date().toISOString(),
  } as unknown as Card
  return { ...base, ...overrides }
}

