function serverModule(context: ScaffoldContext) {
  return `import 'dotenv/config'
import { pathToFileURL } from 'node:url'
import { serve } from '@hono/node-server'
import { Hono } from 'hono'
import {
  decodeShadowServerAppLaunchTokenHint,
  fetchShadowServerAppLaunchInboxes,
  type ShadowServerAppCommandContext,
  type ShadowServerAppCommandName,
} from '@shadowob/sdk'
import { commands } from './commands.js'
import { readState } from './data.js'
import { manifest, shadowApp } from './manifest.js'
import { shadowServerAppManifest } from './shadow-app.generated.js'
import { renderAppPage } from './ui.js'

type AppCommandName = ShadowServerAppCommandName<typeof shadowServerAppManifest>

export const app = new Hono()
const port = Number(process.env.PORT ?? ${context.port})
const commandNames = new Set<string>(shadowServerAppManifest.commands.map((command) => command.name))

function commandName(value: string): AppCommandName | null {
  return commandNames.has(value) ? (value as AppCommandName) : null
}

function shadowApiBaseUrl() {
  return (process.env.SHADOW_SERVER_URL ?? 'http://localhost:3002').replace(/\\/$/, '')
}

type LaunchIntrospection = {
  active?: boolean
  shadow?: Partial<ShadowServerAppCommandContext>
}

function launchActor(
  actor: Partial<ShadowServerAppCommandContext>['actor'] | undefined,
): ShadowServerAppCommandContext['actor'] {
  if (actor && typeof actor.kind === 'string') {
    return {
      kind: actor.kind,
      userId: actor.userId ?? null,
      buddyAgentId: actor.buddyAgentId ?? null,
      ownerId: actor.ownerId ?? null,
      profile: actor.profile ?? null,
    }
  }
  return { kind: 'unknown', userId: null }
}

async function launchCommandContext(
  name: AppCommandName,
  launchToken: string | null | undefined,
): Promise<ShadowServerAppCommandContext | null> {
  const hint = decodeShadowServerAppLaunchTokenHint(launchToken)
  if (!hint || !launchToken) return null
  const response = await fetch(
    \`\${shadowApiBaseUrl()}/api/servers/\${encodeURIComponent(hint.serverId)}/apps/\${encodeURIComponent(
      hint.appKey,
    )}/launch/introspect\`,
    {
      method: 'POST',
      headers: { Authorization: \`Bearer \${launchToken}\` },
    },
  ).catch(() => null)
  if (!response?.ok) return null
  const payload = (await response.json().catch(() => null)) as LaunchIntrospection | null
  if (!payload?.active || !payload.shadow) return null
  const command = shadowServerAppManifest.commands.find((item) => item.name === name)
  const shadow = payload.shadow
  return {
    protocol: 'shadow.app/1',
    serverId: shadow.serverId ?? hint.serverId,
    serverAppId: shadow.serverAppId ?? 'launch',
    appKey: shadow.appKey ?? hint.appKey,
    command: name,
    actor: launchActor(shadow.actor),
    channelId: shadow.channelId ?? null,
    resources: shadow.resources ?? null,
    task: shadow.task,
    permission: command?.permission ?? shadow.permission ?? 'server_app.runtime',
    action: command?.action ?? shadow.action ?? 'read',
    dataClass: command?.dataClass ?? shadow.dataClass ?? 'server-private',
  }
}

function iconSvg() {
  return \`<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 96 96">
  <rect width="96" height="96" rx="22" fill="#0f766e"/>
  <path d="M25 31h46v34H25z" fill="#fff"/>
  <path d="M34 42h28M34 54h18" stroke="#0f766e" stroke-width="6" stroke-linecap="round"/>
</svg>\`
}

app.get('/healthz', (c) => c.json({ ok: true, appKey: ${JSON.stringify(context.appKey)} }))
app.get('/.well-known/shadow-app.json', (c) => c.json(manifest()))
app.get('/assets/icon.svg', (c) => c.body(iconSvg(), 200, { 'Content-Type': 'image/svg+xml' }))
app.get('/shadow/server', (c) => c.html(renderAppPage()))
app.get('/api/state', (c) => c.json(readState()))

app.get('/api/runtime/inboxes', async (c) => {
  const launchToken = c.req.header('X-Shadow-Launch-Token')
  if (!launchToken) return c.json({ ok: false, error: 'missing_launch_token' }, 401)
  try {
    return c.json(
      await fetchShadowServerAppLaunchInboxes({
        launchToken,
        shadowApiBaseUrl: shadowApiBaseUrl(),
      }),
    )
  } catch (error) {
    return c.json({ ok: false, error: error instanceof Error ? error.message : 'inbox_lookup_failed' }, 502)
  }
})

app.post('/api/runtime/commands/:commandName', async (c) => {
  const name = commandName(c.req.param('commandName'))
  if (!name) return c.json({ ok: false, error: 'command_not_found' }, 404)
  let body: { input?: unknown }
  try {
    body = (await c.req.json()) as { input?: unknown }
  } catch {
    return c.json({ ok: false, error: 'invalid_json' }, 400)
  }
  const context = await launchCommandContext(name, c.req.header('X-Shadow-Launch-Token'))
  if (!context) return c.json({ ok: false, error: 'invalid_launch_token' }, 401)
  const result = await shadowApp.executeLocal(name, body.input ?? {}, context, commands)
  return c.json(result.body, result.status as 200)
})

app.post('/api/shadow/commands/:commandName', async (c) => {
  const name = commandName(c.req.param('commandName'))
  if (!name) return c.json({ ok: false, error: 'command_not_found' }, 404)
  const result = await shadowApp.executeCommand(
    name,
    {
      authorizationHeader: c.req.header('authorization'),
      serverIdHeader: c.req.header('X-Shadow-Server-Id'),
      appKeyHeader: c.req.header('X-Shadow-App-Key'),
      requestBody: await c.req.text(),
    },
    commands,
  )
  return c.json(result.body, result.status as 200)
})

export function startStandalone() {
  serve({ fetch: app.fetch, port })
  console.log(\`${context.appName} listening on http://localhost:\${port}\`)
}

if (import.meta.url === pathToFileURL(process.argv[1] ?? '').href) {
  startStandalone()
}
`
}

