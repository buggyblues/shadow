export interface GenerateServerAppScaffoldOptions {
  appKey: string
  directory?: string
  name?: string
  description?: string
  port?: number
  force?: boolean
}

export interface GenerateServerAppScaffoldResult {
  directory: string
  files: string[]
}

interface ScaffoldContext {
  appKey: string
  appName: string
  description: string
  port: number
  updatedAt: string
  envPrefix: string
  permissionPrefix: string
}

const APP_KEY_PATTERN = /^[a-z][a-z0-9-]{1,62}[a-z0-9]$/
const require = createRequire(import.meta.url)

function shadowSdkPackageSpec() {
  try {
    const pkg = require('../../package.json') as { version?: string }
    return pkg.version ? `^${pkg.version}` : 'latest'
  } catch {
    return 'latest'
  }
}

function titleCaseAppKey(appKey: string) {
  return appKey
    .split('-')
    .filter(Boolean)
    .map((part) => `${part.slice(0, 1).toUpperCase()}${part.slice(1)}`)
    .join(' ')
}

function envPrefix(appKey: string) {
  return appKey.replace(/[^a-zA-Z0-9]+/g, '_').toUpperCase()
}

function assertAppKey(appKey: string) {
  if (!APP_KEY_PATTERN.test(appKey)) {
    throw new Error(
      'App key must be 3-64 lowercase letters, numbers, and hyphens; start with a letter and end with a letter or number.',
    )
  }
}

