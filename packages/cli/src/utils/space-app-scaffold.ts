function dataModule(context: ScaffoldContext) {
  return `import { resolve } from 'node:path'
import { createShadowSpaceAppJsonStore } from '@shadowob/sdk/space-app/node'

export interface AppState {
  schemaVersion: '${context.appKey}.state/1'
  createdAt: string
  updatedAt: string
}

function now() {
  return new Date().toISOString()
}

const store = createShadowSpaceAppJsonStore<AppState>({
  filePath: resolve(process.env.${context.envPrefix}_DATA_FILE ?? './data/app-state.json'),
  defaultValue: () => {
    const timestamp = now()
    return {
      schemaVersion: '${context.appKey}.state/1',
      createdAt: timestamp,
      updatedAt: timestamp,
    }
  },
})

export function readState() {
  return store.read()
}

export function touchState() {
  return store.update((state) => {
    state.updatedAt = now()
  })
}
`
}

