function scaffoldFiles(context: ScaffoldContext): Record<string, string> {
  return {
    'README.md': readme(context),
    'package.json': packageJson(context),
    'pnpm-workspace.yaml': pnpmWorkspaceYaml(),
    'tsconfig.json': tsconfigJson(),
    'space-app.local.json': `${JSON.stringify(manifest(context), null, 2)}\n`,
    '.env.example': envExample(context),
    Dockerfile: dockerfile(),
    'src/space-app.generated.ts': generatedManifestModule(context),
    'src/manifest.ts': manifestModule(context),
    'src/commands.ts': commandsModule(),
    'src/data.ts': dataModule(context),
    'src/ui.ts': uiModule(context),
    'src/server.ts': serverModule(context),
  }
}

async function pathExists(path: string) {
  try {
    await stat(path)
    return true
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code === 'ENOENT') return false
    throw error
  }
}

export async function generateSpaceAppScaffold(
  options: GenerateSpaceAppScaffoldOptions,
): Promise<GenerateSpaceAppScaffoldResult> {
  const appKey = options.appKey.trim()
  assertAppKey(appKey)

  const port = options.port ?? 4201
  if (!Number.isInteger(port) || port < 1 || port > 65535) {
    throw new Error('--port must be an integer between 1 and 65535')
  }

  const directory = resolve(options.directory ?? appKey)
  const context: ScaffoldContext = {
    appKey,
    appName: options.name?.trim() || titleCaseAppKey(appKey),
    description:
      options.description?.trim() ||
      'A Space App scaffold. Replace this description before publishing.',
    port,
    updatedAt: new Date().toISOString(),
    envPrefix: envPrefix(appKey),
    permissionPrefix: appKey,
  }
  const files = scaffoldFiles(context)
  const conflicts = []
  for (const relativePath of Object.keys(files)) {
    const target = resolve(directory, relativePath)
    if (await pathExists(target)) conflicts.push(relativePath)
  }
  if (conflicts.length > 0 && !options.force) {
    throw new Error(
      `Refusing to overwrite existing scaffold file(s): ${conflicts.join(', ')}. Pass --force to overwrite.`,
    )
  }

  await mkdir(directory, { recursive: true })
  for (const [relativePath, content] of Object.entries(files)) {
    const target = resolve(directory, relativePath)
    await mkdir(dirname(target), { recursive: true })
    await writeFile(target, content.endsWith('\n') ? content : `${content}\n`)
  }

  return {
    directory,
    files: Object.keys(files).map((file) => relative(process.cwd(), resolve(directory, file))),
  }
}
