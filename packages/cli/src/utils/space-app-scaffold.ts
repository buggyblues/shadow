import { mkdir, stat, writeFile } from 'node:fs/promises'
import { createRequire } from 'node:module'
import { dirname, relative, resolve } from 'node:path'

export interface GenerateSpaceAppScaffoldOptions {
  appKey: string
  directory?: string
  name?: string
  description?: string
  port?: number
  force?: boolean
}

export interface GenerateSpaceAppScaffoldResult {
  directory: string
  files: string[]
}

interface ScaffoldContext {
  appKey: string
  appName: string
  description: string
  port: number
  updatedAt: string
  envPrefix: string
  permissionPrefix: string
}

const APP_KEY_PATTERN = /^[a-z][a-z0-9-]{1,62}[a-z0-9]$/
const require = createRequire(import.meta.url)

function shadowSdkPackageSpec() {
  try {
    const pkg = require('../../package.json') as { version?: string }
    return pkg.version ? `^${pkg.version}` : 'latest'
  } catch {
    return 'latest'
  }
}

function titleCaseAppKey(appKey: string) {
  return appKey
    .split('-')
    .filter(Boolean)
    .map((part) => `${part.slice(0, 1).toUpperCase()}${part.slice(1)}`)
    .join(' ')
}

function envPrefix(appKey: string) {
  return appKey.replace(/[^a-zA-Z0-9]+/g, '_').toUpperCase()
}

function assertAppKey(appKey: string) {
  if (!APP_KEY_PATTERN.test(appKey)) {
    throw new Error(
      'Space App key must be 3-64 lowercase letters, numbers, and hyphens; start with a letter and end with a letter or number.',
    )
  }
}

function manifest(context: ScaffoldContext) {
  const baseUrl = `http://localhost:${context.port}`
  return {
    schemaVersion: 'shadow.space-app/1',
    appKey: context.appKey,
    name: context.appName,
    description: context.description,
    version: '0.1.0',
    updatedAt: context.updatedAt,
    iconUrl: `${baseUrl}/assets/icon.svg`,
    iframe: {
      entry: `${baseUrl}/shadow/server`,
      allowedOrigins: [baseUrl],
    },
    api: {
      baseUrl,
      auth: {
        type: 'oauth2-bearer',
      },
    },
    access: {
      defaultPermissions: [`${context.permissionPrefix}.status:read`],
      defaultApprovalMode: 'none',
    },
    commands: [
      {
        name: 'status.get',
        title: 'Get status',
        description: 'Return a minimal status payload for validating the app runtime.',
        ingress: {
          path: '/.shadow/commands/status.get',
          auth: 'shadow-command-jwt',
        },
        permission: `${context.permissionPrefix}.status:read`,
        action: 'read',
        dataClass: 'server-private',
        inputSchema: {
          type: 'object',
          properties: {},
          additionalProperties: false,
        },
      },
    ],
    skills: [
      {
        name: 'Runtime validation',
        description:
          'Use the status.get command to verify that the app is installed, reachable, and authorized before adding domain-specific commands.',
        commandHints: ['status.get'],
      },
    ],
  }
}

